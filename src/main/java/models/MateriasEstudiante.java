package models;

public class MateriasEstudiante {
	
	private int id_materia;
	private String nombre_materia;
	private String año_materia;
	private String cuatrimestre_materia;
	private String estado_materia;
	
	public MateriasEstudiante(){
		
	}

	public int getId_materia() {
		return id_materia;
	}

	public void setId_materia(int id_materia) {
		this.id_materia = id_materia;
	}

	public String getNombre_materia() {
		return nombre_materia;
	}

	public void setNombre_materia(String nombre_materia) {
		this.nombre_materia = nombre_materia;
	}

	public String getAño_materia() {
		return año_materia;
	}

	public void setAño_materia(String año_materia) {
		this.año_materia = año_materia;
	}

	public String getCuatrimestre_materia() {
		return cuatrimestre_materia;
	}

	public void setCuatrimestre_materia(String cuatrimestre_materia) {
		this.cuatrimestre_materia = cuatrimestre_materia;
	}

	public String getEstado_materia() {
		return estado_materia;
	}

	public void setEstado_materia(String estado_materia) {
		this.estado_materia = estado_materia;
	}
	
	
	
	
}
